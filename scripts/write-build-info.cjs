/* ============================================================================
   write-build-info.cjs
   ----------------------------------------------------------------------------
   BUILD_ID: AUTO-BUILD-INFO-01
   FILE: scripts/write-build-info.cjs

   Purpose
   - Generate build metadata before each production build
   - Write a small TypeScript module consumed by the About page

   Output
   - src/build/buildInfo.ts
   ============================================================================ */

const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

const repoRoot = process.cwd();
const packageJsonPath = path.join(repoRoot, "package.json");
const outputDir = path.join(repoRoot, "src", "build");
const outputFile = path.join(outputDir, "buildInfo.ts");

function safeExec(command, fallback = "unknown") {
  try {
    return execSync(command, { stdio: ["ignore", "pipe", "ignore"] })
      .toString()
      .trim();
  } catch {
    return fallback;
  }
}

function getPackageVersion() {
  const raw = fs.readFileSync(packageJsonPath, "utf8");
  const pkg = JSON.parse(raw);
  return pkg.version || "0.0.0";
}

function toIsoLocal(date) {
  const pad = (n) => String(n).padStart(2, "0");

  const yyyy = date.getFullYear();
  const mm = pad(date.getMonth() + 1);
  const dd = pad(date.getDate());
  const hh = pad(date.getHours());
  const min = pad(date.getMinutes());
  const ss = pad(date.getSeconds());

  return `${yyyy}-${mm}-${dd} ${hh}:${min}:${ss}`;
}

const version = getPackageVersion();
const gitCommit = safeExec("git rev-parse --short HEAD");
const gitBranch = safeExec("git rev-parse --abbrev-ref HEAD");
const builtAt = toIsoLocal(new Date());

const fileContents = `/* ============================================================================
   buildInfo.ts
   ----------------------------------------------------------------------------
   AUTO-GENERATED FILE — DO NOT EDIT BY HAND
   Generated by: scripts/write-build-info.cjs
   ============================================================================ */

export const buildInfo = {
  version: "v${version}",
  commit: "${gitCommit}",
  branch: "${gitBranch}",
  builtAt: "${builtAt}",
} as const;
`;

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputFile, fileContents, "utf8");

console.log(`Wrote build info to ${path.relative(repoRoot, outputFile)}`);